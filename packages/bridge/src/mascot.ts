export function renderMascot(grid: string[], isTTY: boolean = process.stdout.isTTY) {
  return grid.map((row) => {
    return [...row].map((cell) => {
      if (cell === ".") return "  ";
      if (!isTTY) return cell === "W" ? "  " : "██"; // Fallback for tests
      const bg = cell === "#" ? "48;2;64;64;64" : "48;2;255;255;255";
      return `\x1b[${bg}m  \x1b[0m`;
    }).join("");
  }).join("\n");
}
